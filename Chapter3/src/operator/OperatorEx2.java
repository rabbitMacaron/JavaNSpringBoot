package operator;

public class OperatorEx2 {

	public static void main(String[] args) {
		int n = 5%3;
		System.out.println(n);
		
		
		int num1 = 10;
		num1 += 2;
		
		System.out.println(num1);
		
		
		
		
		int score = 100;
		
		System.out.println(++score);
		System.out.println(score++);
		System.out.println(score);
		
		System.out.println(--score);
	}
}
